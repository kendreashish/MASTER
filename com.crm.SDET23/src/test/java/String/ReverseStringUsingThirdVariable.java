package String;

public class ReverseStringUsingThirdVariable 
{
	public static void main(String[] args) 
	{
		{
			String rev="";
			String s="Tyss";
			System.out.println(s);
			for(int i=s.length()-1;i>=0;i--)
					{
						rev=rev+s.charAt(i);
				
					}
				
				System.out.println(rev);
			}
		
		
		
	}

}
